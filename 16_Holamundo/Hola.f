C usamos sintacis de FORTRAN 77.
C 
C Fortran 77 usa la letra C pára identificar COMENTARIOS.
C no son usados ni considerados en un algritmo 

C la primera columna esta reservada para el 
C caracter C o *, que indica comentario 
C la scomunas del 2-5 etiqueta de control de flujo
C la comuna 6 esta reservada para el
C caracter continuación de lineas & O +
C los comandos y sentencias de llenguaje FORTRAN
C inican en la columna 7
C de las comunas 7-72  es para las sentencias

       program holamundo
       character a*4/"hola"/,b*5/"Mundo"/
       character c*18
       
       real r2,preal,pimag
       double precision rr2
       
       complex cr2,z
       
       r2= sqrt(2.0)
       rr2=sqrt(2.0)
       
       z=(0.0,1.0)
       cr2=2.0+csqrt(z)
       
       preal=REAL(Z)
       pimag=AIMAG(z)
       
       write(*,*) a,b
       
*concatenamos dos cadenas de caracteres
       c=a//" "//b
       write (*,*)c
       
       write(*,*) "Yo soy la raiz de 2 de simple precision:",r2
       write(*,*) "Yo soy la raiz de 2 de doble precision:", rr2
       
       write(*,*)"yo soy complejo ¡Hurra! :( ", cr2
       write(*,*)"yo soy la parte real :) ", preal
       write(*,*)"yo soy el amigo imaginario! :(", pimag
       stop
       end
       
